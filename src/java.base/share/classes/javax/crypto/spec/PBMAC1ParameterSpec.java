package javax.crypto.spec;

import java.security.spec.AlgorithmParameterSpec;

/**
 * This class specifies the set of parameters used with PBMAC1,
 * as defined in the
 * <a href="http://www.ietf.org/rfc/rfc9579.txt">PKCS #12</a>
 * standard.
 *
 * @spec https://www.rfc-editor.org/info/rfc9579
 *      RFC 9579: Use of Password-Based Message Authentication Code 1 (PBMAC1) in PKCS #12
 *
 * @since 26
 */
public class PBMAC1ParameterSpec implements AlgorithmParameterSpec {

    private final byte[] salt;
    private final int iterationCount;
    private String kdfHmac = null;
    private String hmac = null;
    private int keyLength;

    /**
     * Constructs a parameter set for PBMAC1 as defined in
     * the PKCS #5 standard.
     *
     * @param salt the salt. The contents of {@code salt} are copied
     * to protect against subsequent modification.
     * @param iterationCount the iteration count
     * @param kdfHmac the HMAC used by PBKDF2
     * @param hmac the HMAC that protects the keystore
     * @param keyLength bit length of Hmac key
     * @exception NullPointerException if {@code salt} is null
     */
    public PBMAC1ParameterSpec(byte[] salt, int iterationCount,
            String kdfHmac, String hmac, int keyLength) {
        this.salt = salt.clone();
        this.iterationCount = iterationCount;
        this.kdfHmac = kdfHmac;
        this.hmac = hmac;
        this.keyLength = keyLength;
    }

    /**
     * Returns the salt.
     *
     * @return the salt. Returns a new array
     * each time this method is called.
     */
    public byte[] getSalt() {
        return salt.clone();
    }

    /**
     * Returns the iteration count.
     *
     * @return the iteration count
     */
    public int getIterationCount() {
        return iterationCount;
    }

    /**
     * Returns Hmac used by PBKDF2.
     *
     * @return the Hmac used by PBKDF2.
     */
    public String getkdfHmac() {
        return kdfHmac;
    }

    /**
     * Returns Hmac used to protect the KeyStore.
     *
     * @return the Hmac
     */
    public String getHmac() {
        return hmac;
    }

    /**
     * Returns length of key generated by PBKDF2.
     *
     * @return length of key generated by PBKDF2.
     */
    public int getKeyLength() {
        return keyLength;
    }
}
